    public void popuniArtikle() {
        try {
            ArrayList<Artikal> artikli = ClientController.getInstance().getAllArtikal();

            cmbArtikal.removeAllItems();

            for (Artikal artikal : artikli) {
                cmbArtikal.addItem(artikal);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void popuniKupce() {
        try {
            ArrayList<Kupac> kupci = ClientController.getInstance().getAllKupac();

            cmbKupac.removeAllItems();

            for (Kupac kupac : kupci) {
                cmbKupac.addItem(kupac);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void resetujFormu() {
        txtAdresa.setText("");
        txtCenaStavke.setText("");
        txtDatumIsporuke.setText("");
        txtKolicina.setText("");
        txtKonacnaCena.setText("");
        konacnaCena = 0;
        TableModelStavke tm = (TableModelStavke) tblStavke.getModel();
        tm.getLista().clear();
        tm.fireTableDataChanged();
    }

    public void osveziTabeluNarudzbina() {
        TableModelNarudzbine tm = (TableModelNarudzbine) tblNarudzbine.getModel();
        tm.refreshTable();
    }

