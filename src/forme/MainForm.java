    private void miOdjavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miOdjavaActionPerformed
        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "se odjavite?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().logout(ulogovani);
                new LoginForma().setVisible(true);
                Session.getInstance().setUlogovani(null);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_miOdjavaActionPerformed

    private void txtKolicinaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKolicinaKeyReleased

        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {

            Artikal artikal = (Artikal) cmbArtikal.getSelectedItem();
            int kolicina = Integer.parseInt(txtKolicina.getText());

            if (kolicina == 0) {
                txtKolicina.setText("");
                txtCenaStavke.setText("");
                return;
            }

            txtCenaStavke.setText(String.valueOf(artikal.getCena() * kolicina));

        } else {
            txtKolicina.setText("");
            txtCenaStavke.setText("");
        }

    }//GEN-LAST:event_txtKolicinaKeyReleased

    private void cmbArtikalItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbArtikalItemStateChanged

        if (cmbArtikal.getSelectedItem() != null) {

            if (txtKolicina.getText().isEmpty()) {
                txtCenaStavke.setText("");
                return;
            }

            Artikal artikal = (Artikal) cmbArtikal.getSelectedItem();
            int kolicina = Integer.parseInt(txtKolicina.getText());

            if (kolicina == 0) {
                txtKolicina.setText("");
                txtCenaStavke.setText("");
                return;
            }

            txtCenaStavke.setText(String.valueOf(artikal.getCena() * kolicina));
        }

    }//GEN-LAST:event_cmbArtikalItemStateChanged

    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed

        if (txtKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu stavke!");
            return;
        }

        Artikal artikal = (Artikal) cmbArtikal.getSelectedItem();
        int kolicina = Integer.parseInt(txtKolicina.getText());
        double cena = Double.parseDouble(txtCenaStavke.getText());

        StavkaNarudzbine sn = new StavkaNarudzbine(null, -1, kolicina, cena, artikal);

        TableModelStavke tm = (TableModelStavke) tblStavke.getModel();
        tm.dodajStavku(sn);

        konacnaCena = tm.vratiKonacnuCenu();
        txtKonacnaCena.setText(String.valueOf(konacnaCena) + "din");

    }//GEN-LAST:event_btnDodajStavkuActionPerformed

    private void btnObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiStavkuActionPerformed

        int row = tblStavke.getSelectedRow();

        if (row >= 0) {
            TableModelStavke tm = (TableModelStavke) tblStavke.getModel();
            tm.obrisiStavku(row);

            konacnaCena = tm.vratiKonacnuCenu();
            txtKonacnaCena.setText(String.valueOf(konacnaCena) + "din");
        }

    }//GEN-LAST:event_btnObrisiStavkuActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        try {
            if (txtAdresa.getText().isEmpty() || txtDatumIsporuke.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumIsporuke = sdf.parse(txtDatumIsporuke.getText());
            String adresa = txtAdresa.getText();
            String grad = (String) cmbGrad.getSelectedItem();
            Kupac kupac = (Kupac) cmbKupac.getSelectedItem();

            TableModelStavke tm = (TableModelStavke) tblStavke.getModel();

            Narudzbina narudzbina = new Narudzbina(null, new Date(), datumIsporuke, grad, adresa,
                    konacnaCena, kupac, ulogovani, tm.getLista());

            ClientController.getInstance().addNarudzbina(narudzbina);
            osveziTabeluNarudzbina();
            resetujFormu();
            JOptionPane.showMessageDialog(this, "Uspesno sacuvana narudzbina!");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

