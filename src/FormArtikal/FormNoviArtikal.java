    private void popuniTipoveArtikla() {

        try {
            ArrayList<TipArtikla> tipoviArtikla = ClientController.getInstance().getAllTipArtikla();

            cmbTipArtikla.removeAllItems();

            for (TipArtikla tipArtikla : tipoviArtikla) {
                cmbTipArtikla.addItem(tipArtikla);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNoviArtikal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
