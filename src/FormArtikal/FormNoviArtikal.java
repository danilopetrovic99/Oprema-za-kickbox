    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            if (txtCena.getText().isEmpty() || txtNaziv.getText().isEmpty()
                    || txtOpis.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String naziv = txtNaziv.getText();
            String opis = txtOpis.getText();
            double cena = Double.parseDouble(txtCena.getText());
            TipArtikla ta = (TipArtikla) cmbTipArtikla.getSelectedItem();

            Artikal a = new Artikal(null, naziv, opis, cena, ta);

            ClientController.getInstance().addArtikal(a);
            MainForm mf = (MainForm) getParent();
            mf.popuniArtikle();
            JOptionPane.showMessageDialog(this, "Uspesno dodat artikal.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

